    private QuizScoreResult scoreQuiz(AuditableQuiz quiz, CefrLevel sentenceLevel,
            Map<LemmaAndPos, CefrLevel> lemmaExpectedLevel) {
        if (quiz == null || quiz.getTokens() == null) {
            return new QuizScoreResult(1.0, Collections.emptyList());
        }
        double discountPerLevel = lemmaAbsenceConfig.getDiscountPerLevel();
        double maxDiscount = 0.0;
        int sentenceOrder = LEVEL_ORDER.get(sentenceLevel);
        List<Map<String, String>> misplacedLemmas = new ArrayList<>();
        Set<String> seen = new HashSet<>();

        for (NlpToken token : quiz.getTokens()) {
            if (token == null || token.getLemma() == null) continue;
            LemmaAndPos lp = new LemmaAndPos(token.getLemma(), token.getPosTag());
            CefrLevel expectedLevel = lemmaExpectedLevel.get(lp);
            if (expectedLevel == null) {
                // Not in EVP — not misplaced (R020)
                continue;
            }
            int expectedOrder = LEVEL_ORDER.get(expectedLevel);
            if (expectedOrder == sentenceOrder) {
                // Correctly placed — no discount (R020)
                continue;
            }
            int distance = Math.abs(sentenceOrder - expectedOrder);
            double discount = discountPerLevel * distance;
            if (discount > maxDiscount) {
                maxDiscount = discount;
            }
            // Track misplaced lemma details (deduplicate by lemma+pos)
            String key = lp.getLemma() + "|" + lp.getPos();
            if (seen.add(key)) {
                Map<String, String> info = new LinkedHashMap<>();
                info.put("lemma", lp.getLemma());
                info.put("pos", lp.getPos());
                info.put("expectedLevel", expectedLevel.name());
                misplacedLemmas.add(info);
            }
        }
        double score = 1.0 - maxDiscount;
        score = Math.max(0.0, Math.min(1.0, score));
        return new QuizScoreResult(score, misplacedLemmas);
    }

    // --- R008/R024: Compute level score weighted by absence type impact ---
    private double computeLevelScore(int totalExpected, int totalAbsent,
            List<AbsentLemma> absentList) {
        if (totalExpected == 0) {
            return 1.0;
        }
        // R008: Each absent lemma contributes its type's impact score to the penalty
        // COMPLETELY_ABSENT=1.0, APPEARS_TOO_LATE=0.8, APPEARS_TOO_EARLY=0.6, SCATTERED=0.4
        double weightedAbsence = 0.0;
        for (AbsentLemma al : absentList) {
            weightedAbsence += al.getAbsenceType().getImpactScore();
        }
        double score = 1.0 - weightedAbsence / totalExpected;
        return Math.max(0.0, Math.min(1.0, score));
    }

    // --- R022, R025: Compute global assessment ---
    private AbsenceAssessment computeAssessment(
            Map<CefrLevel, LevelAbsenceMetrics> levelMetrics,
            Map<CefrLevel, List<AbsentLemma>> absentByLevel) {

        // Count total HIGH priority absent lemmas across all levels
        int totalHighAbsent = 0;
        for (List<AbsentLemma> absentList : absentByLevel.values()) {
            for (AbsentLemma al : absentList) {
                if (al.getPriorityLevel() == PriorityLevel.HIGH) {
                    totalHighAbsent++;
                }
            }
        }

        // Check if any critical level exceeds its threshold (R022)
        boolean anyCriticalExceeds = false;
        boolean anyLevelExceeds = false;
        for (CefrLevel level : CefrLevel.values()) {
            LevelAbsenceMetrics metrics = levelMetrics.get(level);
            boolean exceeds = exceedsThreshold(level, metrics);
            if (exceeds) {
                anyLevelExceeds = true;
                if (CRITICAL_LEVELS.contains(level)) {
                    anyCriticalExceeds = true;
                }
            }
        }

        int criticalThreshold = lemmaAbsenceConfig.getCriticalAbsenceThreshold();
        int acceptableThreshold = lemmaAbsenceConfig.getAcceptableAbsenceThreshold();

        // R025: If totalHighAbsent > criticalThreshold -> cannot be OPTIMAL
        if (anyCriticalExceeds || totalHighAbsent > criticalThreshold) {
            return AbsenceAssessment.NEEDS_IMPROVEMENT;
        }
        if (anyLevelExceeds || totalHighAbsent > acceptableThreshold) {
            return AbsenceAssessment.ACCEPTABLE;
        }
        return AbsenceAssessment.OPTIMAL;
    }

    // R021: Check if a level exceeds its threshold
    private boolean exceedsThreshold(CefrLevel level, LevelAbsenceMetrics metrics) {
        int absoluteThreshold = lemmaAbsenceConfig.getAbsoluteThreshold(level);
        double percentageThreshold = lemmaAbsenceConfig.getPercentageThreshold(level);
        return metrics.getTotalAbsent() > absoluteThreshold
                || metrics.getAbsencePercentage() > percentageThreshold;
    }

    // --- R024: Compute weighted global score ---
    private double computeGlobalScore(Map<CefrLevel, LevelAbsenceMetrics> levelMetrics) {
        double totalWeight = 0.0;
        double weightedSum = 0.0;
        for (CefrLevel level : CefrLevel.values()) {
            double weight = lemmaAbsenceConfig.getLevelWeight(level);
            double levelScore = levelMetrics.get(level).getScore();
            weightedSum += weight * levelScore;
            totalWeight += weight;
        }
        if (totalWeight == 0.0) return 1.0;
        return Math.max(0.0, Math.min(1.0, weightedSum / totalWeight));
    }

    // --- R027-R031: Generate recommendations ---
    private List<AbsenceRecommendation> generateRecommendations(
            Map<CefrLevel, LevelAbsenceMetrics> levelMetrics,
            Map<CefrLevel, List<AbsentLemma>> absentByLevel) {
        List<AbsenceRecommendation> recommendations = new ArrayList<>();

        for (CefrLevel level : CefrLevel.values()) {
            LevelAbsenceMetrics metrics = levelMetrics.get(level);
            if (!exceedsThreshold(level, metrics)) {
                // R027: only generate for levels exceeding their threshold
                continue;
            }

            List<AbsentLemma> absentList = absentByLevel.get(level);
            if (absentList.isEmpty()) {
                continue;
            }

            // Group by absence type and generate one recommendation per dominant type
            Map<AbsenceType, List<AbsentLemma>> byType = new HashMap<>();
            for (AbsentLemma al : absentList) {
                byType.computeIfAbsent(al.getAbsenceType(), k -> new ArrayList<>()).add(al);
            }

            for (Map.Entry<AbsenceType, List<AbsentLemma>> entry : byType.entrySet()) {
                AbsenceType absenceType = entry.getKey();
                List<AbsentLemma> affected = entry.getValue();

                RecommendationAction action = actionForType(absenceType);
                String description = descriptionForType(absenceType, level);
                PriorityLevel recPriority = recommendationPriority(level, affected);
                EffortLevel effort = estimateEffort(affected.size());
                double expectedImpact = computeExpectedImpact(affected, metrics);

                AbsenceRecommendation rec = new AbsenceRecommendation(
                        action, description, recPriority, affected, effort, expectedImpact, level);
                recommendations.add(rec);
            }
        }
        return recommendations;
    }

