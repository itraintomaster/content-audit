    private AssessmentState assess(double actual, double target, TargetKind kind,
            double optimalRange, double adequateRange) {
        if (kind == TargetKind.AT_LEAST) {
            if (actual >= target - optimalRange) {
                return AssessmentState.OPTIMAL;
            } else if (actual >= target - adequateRange) {
                return AssessmentState.ADEQUATE;
            } else {
                return AssessmentState.DEFICIENT;
            }
        } else { // AT_MOST
            if (actual <= target + optimalRange) {
                return AssessmentState.OPTIMAL;
            } else if (actual <= target + adequateRange) {
                return AssessmentState.ADEQUATE;
            } else {
                return AssessmentState.EXCESSIVE;
            }
        }
    }

    private double computeScore(double actual, double target, AssessmentState assessment,
            double adequateRange) {
        switch (assessment) {
            case OPTIMAL:
                return 1.0;
            case ADEQUATE:
                return 0.8;
            default: {
                double distance = Math.abs(actual - target);
                return Math.max(0.0, 0.8 - (distance - adequateRange) * 0.04);
            }
        }
    }

    private double averageBucketScore(List<BucketResult> bucketResults) {
        if (bucketResults == null || bucketResults.isEmpty()) {
            return 0.0;
        }
        return bucketResults.stream()
                .filter(r -> r.getAssessment() != null)
                .mapToDouble(BucketResult::getScore)
                .average()
                .orElse(0.0);
    }

    /**
     * Assigns a range [start, end) of topic indices to a quarter (0-based).
     * Topics are divided as evenly as possible; earlier quarters get the extra topics (R017).
     */
    private int[] topicRangeForQuarter(int quarter, int numTopics) {
        int base = numTopics / 4;
        int extra = numTopics % 4;
        // First 'extra' quarters get base+1 topics
        int start = 0;
        for (int q = 0; q < quarter; q++) {
            start += base + (q < extra ? 1 : 0);
        }
        int size = base + (quarter < extra ? 1 : 0);
        return new int[]{start, start + size};
    }

    /**
     * Given the initial and last quarter targets from config, interpolate Q2 and Q3 (R015, R016).
     * Returns a list of 4 QuarterBucketTargets [Q1, Q2, Q3, Q4].
     */
    private List<QuarterBucketTargets> interpolateQuarters(
            List<QuarterBucketTargets> quarterTargetsConfig) {
        if (quarterTargetsConfig == null || quarterTargetsConfig.size() < 2) {
            return List.of();
        }

        List<BucketTarget> q1Targets = quarterTargetsConfig.get(0).getTargets();
        List<BucketTarget> q4Targets = quarterTargetsConfig.get(1).getTargets();

        List<BucketTarget> q2Targets = new ArrayList<>();
        List<BucketTarget> q3Targets = new ArrayList<>();

        for (BucketTarget q1t : q1Targets) {
            String bandName = q1t.getBandName();
            double q1Pct = q1t.getTargetPercentage();
            TargetKind kind = q1t.getKind(); // Inherit kind from Q1 (R016)

            double q4Pct = findTargetPercentage(q4Targets, bandName, q1Pct);

            double q2Pct = q1Pct + (q4Pct - q1Pct) * (1.0 / 3.0);
            double q3Pct = q1Pct + (q4Pct - q1Pct) * (2.0 / 3.0);

            q2Targets.add(new BucketTarget(bandName, q2Pct, kind));
            q3Targets.add(new BucketTarget(bandName, q3Pct, kind));
        }

        List<QuarterBucketTargets> result = new ArrayList<>();
        result.add(new QuarterBucketTargets(q1Targets));
        result.add(new QuarterBucketTargets(q2Targets));
        result.add(new QuarterBucketTargets(q3Targets));
        result.add(new QuarterBucketTargets(q4Targets));
        return result;
    }

    private double findTargetPercentage(List<BucketTarget> targets, String bandName,
            double defaultValue) {
        if (targets == null) {
            return defaultValue;
        }
        for (BucketTarget t : targets) {
            if (bandName.equals(t.getBandName())) {
                return t.getTargetPercentage();
            }
        }
        return defaultValue;
    }

