    private double scoreQuiz(int tokens, TargetRange range) {
        int min = range.getMinTokens();
        int max = range.getMaxTokens();
        int margin = config.getToleranceMargin();

        if (tokens >= min && tokens <= max) {
            return 1.0;
        }

        int distance;
        if (tokens < min) {
            distance = min - tokens;
        } else {
            distance = tokens - max;
        }

        if (distance >= margin) {
            return 0.0;
        }

        return 1.0 - ((double) distance / margin);
    }

