    private int handleTasks(String name, GetTasksFilter filter) {
        // Validate --status if provided
        if (filter != null && filter.getStatus().isPresent()) {
            String statusVal = filter.getStatus().get();
            if (!Set.of("pending", "completed", "skipped").contains(statusVal.toLowerCase())) {
                System.err.println("Invalid value for --status: '" + statusVal + "'. Allowed: pending, completed, skipped");
                return 1;
            }
        }

        // Validate --limit if provided
        if (filter != null && filter.getLimit().isPresent()) {
            int limitVal = filter.getLimit().get();
            if (limitVal < 0) {
                System.err.println("Invalid value for --limit: must be zero or positive");
                return 1;
            }
        }

        // Resolve the plan
        RefinementPlan plan;
        String resolvedPlanId;
        if (filter != null && filter.getPlanId().isPresent() && !filter.getPlanId().get().isBlank()) {
            String reqPlanId = filter.getPlanId().get();
            Optional<RefinementPlan> planOpt = refinementPlanStore.load(reqPlanId);
            if (planOpt.isEmpty()) {
                System.err.println("Plan '" + reqPlanId + "' not found");
                return 1;
            }
            plan = planOpt.get();
            resolvedPlanId = reqPlanId;
        } else {
            Optional<RefinementPlan> planOpt = refinementPlanStore.loadLatest();
            if (planOpt.isEmpty()) {
                System.err.println("No plans available. Run 'content-audit plan' to create one.");
                return 1;
            }
            plan = planOpt.get();
            resolvedPlanId = plan.getId();
        }

        List<RefinementTask> tasks = plan.getTasks() != null ? plan.getTasks() : List.of();

        // Handle get task <id> (single task by id)
        if (name != null && !name.isBlank()) {
            Optional<RefinementTask> found = tasks.stream()
                    .filter(t -> name.equals(t.getId()))
                    .findFirst();
            if (found.isEmpty()) {
                System.err.println("Task '" + name + "' not found in plan '" + resolvedPlanId + "'");
                return 1;
            }
            return printTaskOne(found.get(), resolvedPlanId);
        }

        // Apply filters
        if (filter != null && filter.getStatus().isPresent()) {
            String statusVal = filter.getStatus().get().toUpperCase();
            RefinementTaskStatus statusEnum;
            try {
                statusEnum = RefinementTaskStatus.valueOf(statusVal);
            } catch (IllegalArgumentException e) {
                System.err.println("Invalid value for --status: '" + filter.getStatus().get() + "'. Allowed: pending, completed, skipped");
                return 1;
            }
            tasks = tasks.stream().filter(t -> t.getStatus() == statusEnum).collect(Collectors.toList());
        }

        // Filter by --target (R008)
        if (filter != null && filter.getTarget().isPresent()) {
            AuditTarget targetFilter = filter.getTarget().get();
            tasks = tasks.stream().filter(t -> targetFilter.equals(t.getNodeTarget())).collect(Collectors.toList());
        }

        // Filter by --diagnosis (R008)
        if (filter != null && filter.getDiagnosisKind().isPresent()) {
            DiagnosisKind diagnosisFilter = filter.getDiagnosisKind().get();
            tasks = tasks.stream().filter(t -> diagnosisFilter.equals(t.getDiagnosisKind())).collect(Collectors.toList());
        }

        // Sort
        if (filter != null && filter.isSortByPriority()) {
            tasks = tasks.stream()
                    .sorted(Comparator.comparingInt(RefinementTask::getPriority))
                    .collect(Collectors.toList());
        }

        // Limit (R009: limit 0 returns empty, not error)
        if (filter != null && filter.getLimit().isPresent()) {
            int limitVal = filter.getLimit().get();
            tasks = tasks.stream().limit(limitVal).collect(Collectors.toList());
        }

        if (tasks.isEmpty()) {
            System.out.println("No tasks found.");
            return 0;
        }

        return switch (formatName) {
            case "json" -> printTaskListJson(plan, tasks);
            case "table" -> printTaskListTable(plan, tasks);
            default -> printTaskListText(plan, tasks);
        };
    }

