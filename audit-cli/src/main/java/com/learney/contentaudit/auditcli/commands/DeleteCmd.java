    @Override
    public int delete(String resource, String id) {
        if (resource == null || resource.isBlank()) {
            System.err.println("Error: resource is required. Deletable resources: audit, plan");
            return 1;
        }

        String normalized = normalizeResource(resource);
        if (normalized == null) {
            System.err.println("Unknown resource '" + resource + "'. Known resources: audits, plans, tasks, analyzers");
            return 1;
        }

        if ("tasks".equals(normalized)) {
            System.err.println("Cannot delete task: 'delete task' is not supported. Delete or regenerate the owning plan instead.");
            return 1;
        }
        if ("analyzers".equals(normalized)) {
            System.err.println("Cannot delete analyzer. Only audit and plan are deletable.");
            return 1;
        }

        if (id == null || id.isBlank()) {
            System.err.println("Cannot delete " + normalized.replaceAll("s$", "") + ": id is required");
            return 1;
        }

        return switch (normalized) {
            case "audits" -> deleteAudit(id);
            case "plans" -> deletePlan(id);
            default -> {
                System.err.println("Cannot delete " + resource + ". Only audit and plan are deletable.");
                yield 1;
            }
        };
    }

    private int deleteAudit(String auditId) {
        // Check if any plan references this audit (R002: refuse with list of referencing plan ids)
        List<String> referencingPlans = StoreHelper.listPlanIdsReferencingAudit(baseDir, auditId);
        if (!referencingPlans.isEmpty()) {
            System.err.println("Cannot delete audit '" + auditId + "': it is referenced by plan(s): "
                    + String.join(", ", referencingPlans) + ". Delete or prune those plans first.");
            return 1;
        }

        // Perform the delete via filesystem helper
        boolean deleted = StoreHelper.deleteAudit(baseDir, auditId);
        if (!deleted) {
            System.err.println("No audits found with id '" + auditId + "'");
            return 1;
        }
        System.out.println("Deleted audit: " + auditId);
        return 0;
    }

    private int deletePlan(String planId) {
        // Verify the plan exists
        Optional<RefinementPlan> planOpt = refinementPlanStore.load(planId);
        if (planOpt.isEmpty()) {
            System.err.println("No plans found with id '" + planId + "'");
            return 1;
        }

        boolean deleted = StoreHelper.deletePlan(baseDir, planId);
        if (!deleted) {
            System.err.println("No plans found with id '" + planId + "'");
            return 1;
        }
        System.out.println("Deleted plan: " + planId);
        return 0;
    }

    private String normalizeResource(String r) {
        return switch (r.toLowerCase()) {
            case "audit", "audits" -> "audits";
            case "plan", "plans" -> "plans";
            case "task", "tasks" -> "tasks";
            case "analyzer", "analyzers" -> "analyzers";
            default -> null;
        };
    }
