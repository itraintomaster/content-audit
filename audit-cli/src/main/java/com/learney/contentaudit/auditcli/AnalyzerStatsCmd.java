package com.learney.contentaudit.auditcli;

import com.learney.contentaudit.auditapplication.AnalyzerRegistry;
import com.learney.contentaudit.auditapplication.AuditRunner;
import com.learney.contentaudit.auditdomain.AuditReport;
import java.nio.file.Path;
import java.util.List;
import java.util.concurrent.Callable;
import javax.annotation.processing.Generated;
import picocli.CommandLine.Command;
import picocli.CommandLine.Parameters;

@Generated(
        value = "com.sentinel.SentinelEngine",
        comments = "Generated by Sentinel"
)
@Command(
        name = "stats",
        description = "Show detailed statistics for a specific analyzer on a course.%n%n"
                + "Runs the audit and displays per-level scores, score distribution,%n"
                + "sub-metrics (e.g. quarters for COCA), and the lowest-scoring items.",
        mixinStandardHelpOptions = true,
        footer = {
                "",
                "Examples:",
                "  # Show sentence-length stats with score distribution",
                "  content-audit analyzer stats sentence-length db/english-course",
                "",
                "  # Show COCA stats with per-quarter breakdown",
                "  content-audit analyzer stats coca-buckets-distribution db/english-course",
                "",
                "  # Use env var for course path",
                "  CONTENT_AUDIT_CONTENT_FOLDER=db/english-course content-audit analyzer stats lemma-recurrence",
        }
)
class AnalyzerStatsCmd implements Callable<Integer> {
    private final AnalyzerRegistry analyzerRegistry;

    private final AnalyzerStatsTransformer analyzerStatsTransformer;

    private final AuditRunner auditRunner;

    @Parameters(index = "0", description = "Analyzer name (e.g. sentence-length)")
    private String analyzerName;

    @Parameters(index = "1", description = "Path to course directory", arity = "0..1")
    private String coursePath;

    public AnalyzerStatsCmd(AnalyzerRegistry analyzerRegistry,
            AnalyzerStatsTransformer analyzerStatsTransformer, AuditRunner auditRunner) {
        this.analyzerRegistry = analyzerRegistry;
        this.analyzerStatsTransformer = analyzerStatsTransformer;
        this.auditRunner = auditRunner;
    }

    @Override
    public Integer call() {
        boolean exists = analyzerRegistry.listAnalyzers().stream()
                .anyMatch(a -> analyzerName.equals(a.getName()));
        if (!exists) {
            System.err.println("Error: analyzer not found: " + analyzerName);
            System.err.println("Use 'content-audit analyzer list' to see available analyzers.");
            return 1;
        }

        String resolvedPath = CoursePathResolver.resolve(coursePath);
        if (resolvedPath == null) {
            System.err.println("Error: missing course path. Provide it as argument or set CONTENT_AUDIT_CONTENT_FOLDER.");
            return 1;
        }

        try {
            AuditReport report = auditRunner.runAudit(Path.of(resolvedPath), null);
            AnalyzerStatsView stats = analyzerStatsTransformer.transform(
                    report, analyzerName, analyzerRegistry);
            printStats(stats);
            return 0;
        } catch (RuntimeException e) {
            System.err.println("Error: " + e.getMessage());
            return 1;
        }
    }

    private void printStats(AnalyzerStatsView stats) {
        System.out.printf("=== %s ===%n", stats.getAnalyzerName());
        if (!stats.getAnalyzerDescription().isEmpty()) {
            System.out.println(stats.getAnalyzerDescription());
        }
        System.out.printf("%nScore: %.1f%%  (%d items)%n", stats.getCourseScore() * 100, stats.getItemCount());

        if (!stats.getLevelScores().isEmpty()) {
            System.out.println("\nPer level:");
            for (var entry : stats.getLevelScores().entrySet()) {
                System.out.printf("  %-6s %.1f%%%n", entry.getKey(), entry.getValue() * 100);
            }
        }

        if (!stats.getSubMetricsByLevel().isEmpty()) {
            List<String> subNames = stats.getSubMetricsByLevel().values().stream()
                    .flatMap(m -> m.keySet().stream())
                    .distinct()
                    .sorted()
                    .toList();

            System.out.println("\nPer quarter:");
            System.out.printf("  %-6s", "");
            for (String sub : subNames) {
                System.out.printf("  %7s", sub);
            }
            System.out.println();

            for (var entry : stats.getSubMetricsByLevel().entrySet()) {
                System.out.printf("  %-6s", entry.getKey());
                for (String sub : subNames) {
                    Double score = entry.getValue().get(sub);
                    if (score != null) {
                        System.out.printf("  %6.1f%%", score * 100);
                    } else {
                        System.out.printf("  %7s", "-");
                    }
                }
                System.out.println();
            }
        }

        if (!stats.getScoreDistribution().isEmpty()) {
            System.out.println("\nScore distribution:");
            int maxCount = stats.getScoreDistribution().values().stream()
                    .mapToInt(Integer::intValue).max().orElse(1);
            for (var entry : stats.getScoreDistribution().entrySet()) {
                int barLen = maxCount > 0 ? (entry.getValue() * 30) / maxCount : 0;
                System.out.printf("  %-8s %4d  %s%n",
                        entry.getKey(), entry.getValue(), "\u2588".repeat(barLen));
            }
        }

        if (!stats.getWorstItems().isEmpty()) {
            System.out.println("\nLowest scoring items:");
            for (ScoredItemRow item : stats.getWorstItems()) {
                System.out.printf("  %-25s %.1f%%%n", item.getLabel(), item.getScore() * 100);
            }
        }
    }
}
