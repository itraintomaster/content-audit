    @Override
    public int prune(String resource, int keep) {
        if (resource == null || resource.isBlank()) {
            System.err.println("Error: resource is required. Pruneable resources: audits, plans");
            return 1;
        }

        if (keep < 0) {
            System.err.println("Invalid value for --keep: must be zero or positive");
            return 1;
        }

        String normalized = normalizeResource(resource);
        if (normalized == null) {
            System.err.println("Unknown resource '" + resource + "'. Known resources: audits, plans, tasks, analyzers");
            return 1;
        }

        return switch (normalized) {
            case "audits" -> pruneAudits(keep);
            case "plans" -> prunePlans(keep);
            default -> {
                System.err.println("Cannot prune " + resource + ". Only audits and plans are pruneable.");
                yield 1;
            }
        };
    }

    private int pruneAudits(int keep) {
        List<String> allIds = StoreHelper.listAuditIds(baseDir);
        // listAuditIds returns sorted ascending by filename (timestamp), so most recent is last
        int total = allIds.size();
        if (total <= keep) {
            System.out.println("Kept " + total + " audits, removed 0.");
            return 0;
        }

        // Keep the N most recent (last N in ascending-sorted list)
        int removeCount = total - keep;
        List<String> toRemove = allIds.subList(0, removeCount);

        for (String id : toRemove) {
            StoreHelper.deleteAudit(baseDir, id);
        }

        System.out.println("Kept " + keep + " audits, removed " + removeCount + ".");
        return 0;
    }

    private int prunePlans(int keep) {
        // Plans sorted by createdAt descending (most recent first)
        List<RefinementPlan> allPlans = StoreHelper.listPlansSortedByRecency(baseDir);
        int total = allPlans.size();
        if (total <= keep) {
            System.out.println("Kept " + total + " plans, removed 0.");
            return 0;
        }

        // Keep first `keep` (most recent), remove the rest
        int removeCount = total - keep;
        List<RefinementPlan> toRemove = allPlans.subList(keep, total);

        for (RefinementPlan plan : toRemove) {
            StoreHelper.deletePlan(baseDir, plan.getId());
        }

        System.out.println("Kept " + keep + " plans, removed " + removeCount + ".");
        return 0;
    }

    private String normalizeResource(String r) {
        return switch (r.toLowerCase()) {
            case "audit", "audits" -> "audits";
            case "plan", "plans" -> "plans";
            case "task", "tasks" -> "tasks";
            case "analyzer", "analyzers" -> "analyzers";
            default -> null;
        };
    }
