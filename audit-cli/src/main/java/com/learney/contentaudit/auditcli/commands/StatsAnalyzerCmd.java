    @Override
    public int showStats(String analyzerName, String coursePath) {
        if (analyzerName == null || analyzerName.isBlank()) {
            System.err.println("Error: analyzer name is required. Run 'content-audit get analyzers' to see available analyzers.");
            return 1;
        }

        // Validate analyzer exists
        List<AnalyzerDescriptor> descriptors = analyzerRegistry.listAnalyzers();
        Optional<AnalyzerDescriptor> descriptorOpt = descriptors.stream()
                .filter(d -> analyzerName.equals(d.getName()))
                .findFirst();
        if (descriptorOpt.isEmpty()) {
            System.err.println("Analyzer '" + analyzerName + "' not found. Run 'content-audit get analyzers' to see available analyzers.");
            return 1;
        }

        // Resolve course path (same behavior as FEAT-CLI: CONTENT_AUDIT_CONTENT_FOLDER env var fallback)
        String resolvedCoursePath = CoursePathResolver.resolve(coursePath);
        if (resolvedCoursePath == null) {
            System.err.println("Error: missing course path. Provide it as argument or set CONTENT_AUDIT_CONTENT_FOLDER.");
            return 1;
        }

        try {
            AuditReport report = auditRunner.runAudit(Path.of(resolvedCoursePath), null);
            AnalyzerStatsView view = analyzerStatsTransformer.transform(report, analyzerName, analyzerRegistry);

            if ("json".equals(formatName)) {
                return printJson(view);
            } else {
                return printText(view);
            }
        } catch (Exception e) {
            System.err.println("Error running stats for analyzer '" + analyzerName + "': " + e.getMessage());
            return 1;
        }
    }

    private int printText(AnalyzerStatsView view) {
        if (view == null) {
            System.out.println("No stats available.");
            return 0;
        }
        System.out.println(view.toString());
        return 0;
    }

    private int printJson(AnalyzerStatsView view) {
        try {
            ObjectMapper om = new ObjectMapper();
            om.enable(SerializationFeature.INDENT_OUTPUT);
            System.out.println(om.writeValueAsString(view));
            return 0;
        } catch (Exception e) {
            System.err.println("Error formatting JSON: " + e.getMessage());
            return 1;
        }
    }
