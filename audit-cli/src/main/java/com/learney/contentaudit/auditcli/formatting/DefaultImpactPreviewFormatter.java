package com.learney.contentaudit.auditcli.formatting;

import com.learney.contentaudit.revisiondomain.impactpreview.ImpactPreview;
import com.learney.contentaudit.revisiondomain.impactpreview.ImpactPreviewAvailability;
import com.learney.contentaudit.revisiondomain.impactpreview.LevelImpact;
import com.learney.contentaudit.revisiondomain.impactpreview.DimensionDelta;
import com.learney.contentaudit.revisiondomain.impactpreview.ScoreDelta;
import javax.annotation.processing.Generated;

import java.util.List;
import java.util.stream.Collectors;

/**
 * Convierte un ImpactPreview de dominio (escala 0..1) en un ImpactPreviewView
 * con strings pre-formateados en notacion porcentual para el operador (F-PIPRE-R013).
 *
 * Ejemplo de formato: "72% -> 81% (+9 pp)"
 * - Valores redondeados al entero mas cercano
 * - Diferencia con signo explicito: "+9 pp" para mejora, "-7 pp" para empeora
 */
@Generated(
        value = "com.sentinel.SentinelEngine",
        comments = "Generated by Sentinel"
)
public class DefaultImpactPreviewFormatter implements ImpactPreviewFormatter {

    @Override
    public ImpactPreviewView format(ImpactPreview preview) {
        if (preview == null) {
            return new ImpactPreviewView(ImpactPreviewAvailability.UNAVAILABLE,
                    "Preview no disponible", List.of());
        }

        if (preview.getAvailability() == ImpactPreviewAvailability.UNAVAILABLE) {
            String unavailabilityText = buildUnavailabilityText(preview);
            return new ImpactPreviewView(ImpactPreviewAvailability.UNAVAILABLE,
                    unavailabilityText, List.of());
        }

        // AVAILABLE: convertir cada LevelImpact a LevelImpactView con strings porcentuales
        List<LevelImpactView> levelViews = preview.getLevelImpacts() == null
                ? List.of()
                : preview.getLevelImpacts().stream()
                        .map(this::toLevelImpactView)
                        .collect(Collectors.toList());

        return new ImpactPreviewView(ImpactPreviewAvailability.AVAILABLE, null, levelViews);
    }

    // -------------------------------------------------------------------------
    // Helpers internos
    // -------------------------------------------------------------------------

    private String buildUnavailabilityText(ImpactPreview preview) {
        if (preview.getUnavailability() == null) {
            return "Preview de impacto no disponible";
        }
        String reason = preview.getUnavailability().getReason() != null
                ? preview.getUnavailability().getReason().name()
                : "UNKNOWN";
        String detail = preview.getUnavailability().getDetail();
        if (detail != null && !detail.isBlank()) {
            return "Preview de impacto no disponible [" + reason + "]: " + detail;
        }
        return "Preview de impacto no disponible [" + reason + "]";
    }

    private LevelImpactView toLevelImpactView(LevelImpact level) {
        String aggregateText = formatDelta(level.getAggregateDelta());

        List<DimensionDeltaView> dimensionRows = level.getDimensionDeltas() == null
                ? List.of()
                : level.getDimensionDeltas().stream()
                        .map(this::toDimensionDeltaView)
                        .collect(Collectors.toList());

        return new LevelImpactView(level.getNodeTarget(), level.getNodeId(),
                aggregateText, dimensionRows);
    }

    private DimensionDeltaView toDimensionDeltaView(DimensionDelta dimDelta) {
        String deltaText = formatDelta(dimDelta.getDelta());
        return new DimensionDeltaView(dimDelta.getDimension(), deltaText);
    }

    /**
     * Formatea un ScoreDelta (escala 0..1) como string porcentual con signo en la diferencia.
     * Ejemplo: ScoreDelta(0.72, 0.81, 0.09) → "72% -> 81% (+9 pp)"
     */
    private String formatDelta(ScoreDelta delta) {
        if (delta == null) {
            return "N/D";
        }
        int beforePct = (int) Math.round(delta.getBefore() * 100);
        int afterPct = (int) Math.round(delta.getAfter() * 100);
        int diffPct = (int) Math.round(delta.getDifference() * 100);

        String diffStr = diffPct > 0 ? "+" + diffPct + " pp"
                : (diffPct < 0 ? diffPct + " pp" : "0 pp");

        return beforePct + "% -> " + afterPct + "% (" + diffStr + ")";
    }
}
