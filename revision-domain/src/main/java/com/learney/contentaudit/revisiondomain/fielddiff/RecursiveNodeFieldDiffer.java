    /**
     * Walk an arbitrary object triple by reflecting over the getters of the
     * most-concrete non-null instance. Skips fields excluded by the registry.
     */
    private void walkObject(Object orig, Object cons, Object pend,
            String prefix, Map<String, FieldChange> result) {
        // Determine the type to reflect over (prefer consolidated, fallback to orig/pend)
        Class<?> type = effectiveType(orig, cons, pend);
        if (type == null) {
            return;
        }

        String declaringTypeName = type.getSimpleName();

        for (Method getter : collectGetters(type)) {
            String fieldName = fieldNameFromGetter(getter.getName());
            if (fieldName == null) {
                continue;
            }

            // R020: skip excluded fields
            if (exclusionRegistry.isExcluded(declaringTypeName, fieldName)) {
                continue;
            }

            Object v0 = invokeGetter(getter, orig);
            Object v1 = invokeGetter(getter, cons);
            Object v2 = invokeGetter(getter, pend);

            String path = prefix + "." + fieldName;

            if (isLeaf(v0, v1, v2)) {
                // Leaf scalar: emit only when photos differ
                if (!allEqual(v0, v1, v2)) {
                    result.put(path, new FieldChange(v0, v1, v2));
                }
            } else if (isList(v0, v1, v2)) {
                // R022: diff list — keyed or positional
                Optional<ListIdentityKeySpec> keySpec =
                        identityRegistry.getKeySpec(declaringTypeName, fieldName);
                walkList(toList(v0), toList(v1), toList(v2), path, keySpec.orElse(null), result);
            } else if (isOptional(v0, v1, v2)) {
                // Unwrap Optional values and continue walking
                Object u0 = unwrapOptional(v0);
                Object u1 = unwrapOptional(v1);
                Object u2 = unwrapOptional(v2);
                if (u0 == null && u1 == null && u2 == null) {
                    continue;
                }
                if (isLeafValue(u0) || isLeafValue(u1) || isLeafValue(u2)) {
                    if (!allEqual(u0, u1, u2)) {
                        result.put(path, new FieldChange(u0, u1, u2));
                    }
                } else {
                    walkObject(u0, u1, u2, path, result);
                }
            } else {
                // Composite object: recurse
                if (v0 != null || v1 != null || v2 != null) {
                    walkObject(v0, v1, v2, path, result);
                }
            }
        }
    }

