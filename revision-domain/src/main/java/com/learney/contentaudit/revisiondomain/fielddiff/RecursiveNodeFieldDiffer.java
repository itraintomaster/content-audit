/**
 * Recursive walker that compares three AuditNode photos (original, consolidated,
 * pendingProjection) and emits a {@code Map<String, FieldChange>} where each key
 * is a stable dot-separated path (R023) and each value is the tripleta of
 * (original, consolidated, pendingProjection) leaf values.
 *
 * <p>Design decisions:
 * <ul>
 *   <li>Reflection over getters — avoids coupling to concrete field names and works
 *       uniformly across AuditableEntity impls, NodeDiagnoses impls, etc.</li>
 *   <li>A "leaf" is any value that is neither a Collection nor a recognised
 *       domain-object (i.e. has a simple {@code toString} / is an enum / is a
 *       primitive wrapper / is a String). Objects with getter methods are walked
 *       recursively.</li>
 *   <li>Only fields where not all three photos agree are emitted (R006 invariante 1).</li>
 *   <li>The registries decide which fields to skip (R020) and which lists have a
 *       natural key (R022).</li>
 * </ul>
 */
