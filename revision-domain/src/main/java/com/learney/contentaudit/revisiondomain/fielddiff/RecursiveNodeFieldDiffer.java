    /**
     * Builds a map from natural-key string to element for one list photo.
     * Natural-key fields may be nested (dot-separated, e.g. "lemmaAndPos.lemma").
     */
    private Map<String, Object> indexByKey(List<?> list, List<String> keyFields) {
        if (list == null || list.isEmpty()) {
            return Collections.emptyMap();
        }
        Map<String, Object> index = new LinkedHashMap<>();
        for (Object element : list) {
            String naturalKey = buildNaturalKey(element, keyFields);
            index.put(naturalKey, element);
        }
        return index;
    }

    /**
     * Builds the natural-key string for an element given the declared field paths.
     * Format: {@code k1=v1,k2=v2} (key names in lexicographic order as declared in
     * the registry — the registry already lists them in the canonical order).
     */
    private String buildNaturalKey(Object element, List<String> keyFields) {
        if (element == null) {
            return "null";
        }
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < keyFields.size(); i++) {
            if (i > 0) sb.append(',');
            String fieldPath = keyFields.get(i);
            Object value = resolveNestedField(element, fieldPath);
            // Use last segment of the field path as the display name in the key
            String displayName = fieldPath.contains(".")
                    ? fieldPath.substring(fieldPath.lastIndexOf('.') + 1)
                    : fieldPath;
            sb.append(displayName).append('=').append(value);
        }
        return sb.toString();
    }

    /**
     * Resolves a dot-separated field path on an object using reflection getters.
     */
    private Object resolveNestedField(Object obj, String dotPath) {
        if (obj == null) {
            return null;
        }
        String[] parts = dotPath.split("\\.", 2);
        String first = parts[0];
        Object next = getFieldValue(obj, first);
        if (parts.length == 1 || next == null) {
            return next;
        }
        return resolveNestedField(next, parts[1]);
    }

    private Object getFieldValue(Object obj, String fieldName) {
        if (obj == null) return null;
        String getterName = "get" + Character.toUpperCase(fieldName.charAt(0))
                + fieldName.substring(1);
        try {
            Method m = obj.getClass().getMethod(getterName);
            return m.invoke(obj);
        } catch (NoSuchMethodException e) {
            // try boolean "is" getter
            String isName = "is" + Character.toUpperCase(fieldName.charAt(0))
                    + fieldName.substring(1);
            try {
                Method m = obj.getClass().getMethod(isName);
                return m.invoke(obj);
            } catch (Exception ignored) {
                return null;
            }
        } catch (Exception e) {
            return null;
        }
    }

