    /**
     * Collects all public no-arg getters defined on {@code type} (but not on
     * {@code Object}), excluding well-known infrastructure getters.
     */
    private static List<Method> collectGetters(Class<?> type) {
        List<Method> getters = new ArrayList<>();
        for (Method m : type.getMethods()) {
            if (!m.getDeclaringClass().equals(Object.class)
                    && m.getParameterCount() == 0
                    && (m.getName().startsWith("get") || m.getName().startsWith("is"))
                    && !m.getName().equals("getClass")
                    && !m.getReturnType().equals(void.class)) {
                getters.add(m);
            }
        }
        return getters;
    }

    private static String fieldNameFromGetter(String getterName) {
        if (getterName.startsWith("get") && getterName.length() > 3) {
            return Character.toLowerCase(getterName.charAt(3)) + getterName.substring(4);
        }
        if (getterName.startsWith("is") && getterName.length() > 2) {
            return Character.toLowerCase(getterName.charAt(2)) + getterName.substring(3);
        }
        return null;
    }

    private static Object invokeGetter(Method getter, Object target) {
        if (target == null) return null;
        try {
            return getter.invoke(target);
        } catch (Exception e) {
            return null;
        }
    }

