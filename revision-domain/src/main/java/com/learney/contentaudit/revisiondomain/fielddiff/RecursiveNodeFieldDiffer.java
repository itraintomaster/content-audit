    /**
     * A leaf value is one that should not be walked into — it carries a scalar
     * that can be directly compared and emitted.
     */
    private static boolean isLeafValue(Object v) {
        if (v == null) return true;
        return v instanceof String
                || v instanceof Number
                || v instanceof Boolean
                || v instanceof Enum<?>
                || v instanceof Character;
    }

    /**
     * Returns true if at least one of the three values is a leaf (or all null),
     * meaning the triple should be emitted as a scalar FieldChange rather than
     * walked into.
     */
    private static boolean isLeaf(Object v0, Object v1, Object v2) {
        Object representative = firstNonNull(v0, v1, v2);
        if (representative == null) return true;
        return isLeafValue(representative);
    }

    private static boolean isList(Object v0, Object v1, Object v2) {
        Object representative = firstNonNull(v0, v1, v2);
        return representative instanceof Collection;
    }

    private static boolean isOptional(Object v0, Object v1, Object v2) {
        Object representative = firstNonNull(v0, v1, v2);
        return representative instanceof Optional;
    }

    private static Object unwrapOptional(Object v) {
        if (v instanceof Optional) {
            return ((Optional<?>) v).orElse(null);
        }
        return null;
    }

    @SuppressWarnings("unchecked")
    private static List<Object> toList(Object v) {
        if (v instanceof List) return (List<Object>) v;
        if (v instanceof Collection) return new ArrayList<>((Collection<Object>) v);
        return null;
    }

    private static Object firstNonNull(Object a, Object b, Object c) {
        if (a != null) return a;
        if (b != null) return b;
        return c;
    }

    private static Class<?> effectiveType(Object orig, Object cons, Object pend) {
        Object representative = firstNonNull(cons, orig, pend);
        return representative != null ? representative.getClass() : null;
    }

    private static boolean allEqual(Object a, Object b, Object c) {
        return Objects.equals(a, b) && Objects.equals(b, c);
    }

    private static int maxSize(List<?> a, List<?> b, List<?> c) {
        int sa = a != null ? a.size() : 0;
        int sb = b != null ? b.size() : 0;
        int sc = c != null ? c.size() : 0;
        return Math.max(sa, Math.max(sb, sc));
    }

    private static Object safeGet(List<?> list, int i) {
        if (list == null || i >= list.size()) return null;
        return list.get(i);
    }
