        assertFalse(knowledgeImpact.getFieldChanges().isEmpty(),
                "El mapa fieldChanges del knowledge debe contener al menos un field cambiado (R006, R022)");

        // Ningun campo de orden interno o timestamp (R020)
        boolean tieneExcluidos = view.getNodeImpacts().stream()
                .flatMap(ni -> ni.getFieldChanges().keySet().stream())
                .anyMatch(k -> k.contains("createdAt") || k.contains("_id"));
