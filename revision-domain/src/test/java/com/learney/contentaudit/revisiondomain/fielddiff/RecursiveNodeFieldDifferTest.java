        diagnosesOrig.setLemmaAbsenceDiagnosis(levelDiagOrig);
        DefaultLevelDiagnoses diagnosesCons = new DefaultLevelDiagnoses();
        diagnosesCons.setLemmaAbsenceDiagnosis(levelDiagCons);
