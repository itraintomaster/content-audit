    /**
     * Flattens all entries (and their pipe-splits) into an ordered flat list of variants.
     * Canonical variant is at index 0.
     */
    private List<String> flattenVariants(List<String> options) {
        List<String> flat = new ArrayList<>();
        for (String entry : options) {
            String[] splits = entry.split("\\|", -1);
            flat.addAll(Arrays.asList(splits));
        }
        return flat;
    }

    /**
     * Advances the odometer indices for the next combination.
     * The rightmost (last CLOZE) advances first; carries propagate left.
     */
    private void advanceIndices(int[] indices, List<List<String>> clozeVariants) {
        for (int i = indices.length - 1; i >= 0; i--) {
            indices[i]++;
            if (indices[i] < clozeVariants.get(i).size()) {
                break;
            }
            indices[i] = 0;
        }
    }

