    private static final WhitespaceNormalizer WS = new WhitespaceNormalizer();

    /**
     * Serializes {@code form.sentenceParts} to a canonical quizSentence DSL string.
     *
     * @param form the FormEntity to serialize
     * @return the canonical quizSentence string
     * @throws QuizSentenceSerializationException if the sentenceParts are invalid
     */
    String serialize(FormEntity form) {
        if (form == null || form.getSentenceParts() == null || form.getSentenceParts().isEmpty()) {
            return "";
        }

        List<SentencePartEntity> parts = form.getSentenceParts();
        StringBuilder sb = new StringBuilder();
        boolean lastWasToken = false; // true if previous token ended with non-whitespace content

        for (int i = 0; i < parts.size(); i++) {
            SentencePartEntity part = parts.get(i);
            if (part.getKind() == SentencePartKind.TEXT) {
                validateTextPart(part, i);
                String text = part.getText();
                if (text == null || text.isEmpty()) {
                    continue;
                }
                // Check for reserved characters in TEXT (R009, DOUBT-ESCAPE-CHARS → Option D)
                checkReservedCharsInText(text, i);

                // Trim the text and apply canonical spacing
                String trimmed = text.trim();
                if (trimmed.isEmpty()) {
                    continue;
                }
                if (lastWasToken) {
                    // Ensure single space between prior CLOZE token and this TEXT
                    // The text itself may start with space — we normalize to one space
                    sb.append(" ");
                }
                sb.append(trimmed);
                lastWasToken = true;
            } else if (part.getKind() == SentencePartKind.CLOZE) {
                validateClozePart(part, i);
                String block = buildResponseBlock(part.getOptions());
                if (lastWasToken) {
                    sb.append(" ");
                }
                sb.append(BLANK).append(" ").append("[").append(block).append("]");
                lastWasToken = true;
            } else {
                throw new QuizSentenceSerializationException(
                        "Tipo de parte desconocido en indice " + i + ": " + part.getKind(),
                        String.valueOf(i));
            }
        }

        return sb.toString();
    }

