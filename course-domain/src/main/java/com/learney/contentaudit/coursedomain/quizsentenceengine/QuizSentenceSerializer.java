    private void validateTextPart(SentencePartEntity part, int index) {
        List<String> options = part.getOptions();
        if (options != null && !options.isEmpty()) {
            throw new QuizSentenceSerializationException(
                    "El TEXT de indice " + index + " trae options; los TEXT no pueden tener options",
                    String.valueOf(index));
        }
    }

    private void validateClozePart(SentencePartEntity part, int index) {
        List<String> options = part.getOptions();
        if (options == null || options.isEmpty()) {
            throw new QuizSentenceSerializationException(
                    "El CLOZE de indice " + index
                            + " no tiene options; un CLOZE requiere al menos una respuesta aceptada",
                    String.valueOf(index));
        }
    }

    private void checkReservedCharsInText(String text, int index) {
        if (text.contains("[") || text.contains("]")) {
            throw new QuizSentenceSerializationException(
                    "Caracter reservado '[' o ']' encontrado sin escapar en TEXT de indice " + index,
                    String.valueOf(index));
        }
        if (text.contains(BLANK)) {
            throw new QuizSentenceSerializationException(
                    "Token reservado '____' encontrado sin escapar en TEXT de indice " + index,
                    String.valueOf(index));
        }
    }

