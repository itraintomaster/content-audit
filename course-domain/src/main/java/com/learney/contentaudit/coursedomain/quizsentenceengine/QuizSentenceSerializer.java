/**
 * Converts a {@code FormEntity.sentenceParts} to its canonical {@code quizSentence} DSL string.
 *
 * <p>Rules applied:
 * <ul>
 *   <li>R003 – TEXT parts must not carry options.</li>
 *   <li>R004 – CLOZE parts must have at least one option.</li>
 *   <li>R005/R006 – Blank written as {@code ____} followed by {@code [variants|pipe-joined]}.</li>
 *   <li>R009 – Reserved characters ({@code [}, {@code ]}, four-underscore token) in TEXT
 *              trigger an explicit failure.</li>
 *   <li>R010 – Single canonical space between adjacent tokens.</li>
 *   <li>R011 – TEXT content emitted as-is (hints preserved).</li>
 *   <li>R012 – All entries and all pipe-splits within each entry joined as one pipe-delimited block.</li>
 *   <li>R013/R024 – Any invalid input triggers atomic failure.</li>
 * </ul>
 */
