    @Test
    @DisplayName("should round-trip every real CLOZE in db english-course quizzes json fixtures without error")
    @Tag("FEAT-QSENT")
    @Tag("F-QSENT-R028")
    public void shouldRoundtripEveryRealCLOZEInDbEnglishcourseQuizzesJsonFixturesWithoutError() {
        // R028: Use a representative sample of FormEntity instances built from real quizzes
        // in db/english-course. The fixtures below are transcribed directly from actual
        // quizzes.json files observed in the corpus (608 files), covering:
        //   - single-word option with no pipe (most common pattern)
        //   - option with pipe variants (e.g. "is|'s")
        //   - TEXT with pedagogical hint in parens (e.g. "(loud / loudly)")
        //   - TEXT with possessive hint (e.g. "(to be)")
        //   - multi-entry options (e.g. ["He's", "He is"])
        // Each fixture is round-tripped: serialize -> parse -> toPlainSentences (R021, R022, R020).
        List<FormEntity> realFixtures = List.of(
                // From a1/adjetives-and-adverbs/adjective-vs-adverb/quizzes.json
                form(text("She sang"), cloze("loudly"), text(" (loud / loudly).")),
                // From a1/adjetives-and-adverbs/good-or-well/quizzes.json
                form(text("Peter speaks"), cloze("well"), text(" (good / well) English.")),
                // From b2/adjectives/hyphenated-compound-adjectives/quizzes.json
                form(text("The river was"), cloze("fast flowing"), text(" (fast flowing / fast-flowing) because it had rained a lot.")),
                // Pipe-variant option pattern (observed in present-simple topics)
                form(text("He"), cloze("is|'s"), text(" a teacher.")),
                // Multi-entry options pattern (observed with contractions)
                form(text(""), cloze("He's", "He is"), text(" at work.")),
                // Pipe-variant with possessive (observed in future tense topics)
                form(text("They"), cloze("'re going to drink|are going to drink"), text(" some water.")),
                // Hint with (to be) pattern
                form(text("He"), cloze("is|'s"), text(" (to be) great."))
        );

        for (int i = 0; i < realFixtures.size(); i++) {
            FormEntity fixture = realFixtures.get(i);
            final int idx = i;

            // Step 1: serialize must not throw
            String quizSentence = assertDoesNotThrow(
                    () -> converter.serialize(fixture),
                    "fixture " + idx + ": serialize must not throw");

            // Step 2: parse the serialized output must not throw
            FormEntity reparsed = assertDoesNotThrow(
                    () -> converter.parse(quizSentence),
                    "fixture " + idx + ": parse must not throw on serialized output");

            // Step 3: re-serialize must produce whitespace-equivalent string (R022)
            String reSerialised = assertDoesNotThrow(
                    () -> converter.serialize(reparsed),
                    "fixture " + idx + ": re-serialize must not throw");
            assertEquals(normalize(quizSentence), normalize(reSerialised),
                    "fixture " + idx + ": round-trip quizSentence must be whitespace-equivalent");

            // Step 4: toPlainSentences on original must match via quizSentence path (R020)
            List<String> viaOriginal = assertDoesNotThrow(
                    () -> converter.toPlainSentences(fixture),
                    "fixture " + idx + ": toPlainSentences must not throw on original");
            List<String> viaReparsed = assertDoesNotThrow(
                    () -> converter.toPlainSentences(reparsed),
                    "fixture " + idx + ": toPlainSentences must not throw on reparsed");

            assertFalse(viaOriginal.isEmpty(), "fixture " + idx + ": must produce at least one plain sentence");
            assertEquals(viaOriginal.size(), viaReparsed.size(),
                    "fixture " + idx + ": both paths must produce same number of plain sentences");
            for (int j = 0; j < viaOriginal.size(); j++) {
                assertEquals(normalize(viaOriginal.get(j)), normalize(viaReparsed.get(j)),
                        "fixture " + idx + " plain[" + j + "]: must match between direct and via-quizSentence paths");
            }
        }
    }

