    @Test
    @DisplayName("should continue tokenizing the remaining sentences of a batch and emit a fallback NlpToken (text as-is lemma=lowercased text posTag=X frequencyRank=null isStop=false isPunct=heuristic) when a single token inside SpaCy fails")
    @Tag("FEAT-NLP")
    @Tag("F-NLP-R032")
    public void shouldContinueTokenizingTheRemainingSentencesOfABatchAndEmitAFallbackNlpTokenTextAsisLemmalowercasedTextPosTagXFrequencyRanknullIsStopfalseIsPunctheuristicWhenASingleTokenInsideSpaCyFails(
            ) {
        // R032: a bad token in one sentence produces a fallback token, not a batch failure;
        // the remaining sentences must still be processed.
        SpacyProcessRunner runner = mock(SpacyProcessRunner.class);
        SpacyResultParser parser = new SpacyResultParser();
        // Batch: two sentences. First has a bad token (no lemma/pos — parser uses fallback);
        // second sentence processes normally.
        String badTokenJson = "{\"processed_sentences\":["
                + "{\"original_sentence\":\"bad\",\"processed_words\":[{\"word\":\"???\"}]},"  // missing lemma/pos → fallback
                + "{\"original_sentence\":\"cat\",\"processed_words\":[{\"word\":\"cat\",\"lemma\":\"cat\","
                + "\"pos_tag\":\"NOUN\",\"frequency_rank\":300,\"is_stop_word\":false,\"is_punctuation\":false}]}"
                + "]}";
        when(runner.run(anyList())).thenReturn(badTokenJson);

        SpacyNlpTokenizer sut = new SpacyNlpTokenizer(runner, parser);
        Map<String, List<NlpToken>> result = sut.analyzeTokensBatch(List.of("bad", "cat"));

        // Both sentences must be in the result — no batch-level failure
        assertTrue(result.containsKey("bad"), "R032: bad-token sentence must still appear in result");
        assertTrue(result.containsKey("cat"), "R032: healthy sentence must still appear in result");

        // Fallback token for the problematic word
        List<NlpToken> fallbackTokens = result.get("bad");
        assertFalse(fallbackTokens.isEmpty(), "R032: fallback token must be emitted for bad input");
        NlpToken fallback = fallbackTokens.get(0);
        assertEquals("X", fallback.getPosTag(), "R032: fallback posTag must be 'X'");

        // Healthy token for the normal sentence
        List<NlpToken> goodTokens = result.get("cat");
        assertFalse(goodTokens.isEmpty(), "R032: healthy sentence must produce its tokens");
        assertEquals("NOUN", goodTokens.get(0).getPosTag(), "R032: healthy token posTag intact");
    }
